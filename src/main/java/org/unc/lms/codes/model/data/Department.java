package org.unc.lms.codes.model.data;

public class Department {
	 private String deptid;
	 private String name;
	 
	public String getDeptid() {
		return deptid;
	}
	public void setDeptid(String deptid) {
		this.deptid = deptid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	 
}
 